# Realizar un programa que lea cuatro valores 
# numéricos e informar su suma y promedio.

print("Introduce los números en el orden solicitados")
a=int(input("Introduce el primer número:"))
b=int(input("Introduce el segundo número:"))
c=int(input("Introduce el segundo número:"))
d=int(input("Introduce el segundo número:"))
suma=a+b+c+d
promedio=suma/4

print("La suma de los número es: ",a+b+c+d)
print("El promedio de los 4 números es %2.3f"%(promedio))
